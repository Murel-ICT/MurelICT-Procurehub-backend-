'use server';
/**
 * @fileOverview Generates a description for a procurement kit based on provided keywords.
 *
 * - generateProcurementKitDescription - A function that generates the kit description.
 * - GenerateProcurementKitDescriptionInput - The input type for the generateProcurementKitDescription function.
 * - GenerateProcurementKitDescriptionOutput - The return type for the generateProcurementKitDescription function.
 */

import {ai} from '@/ai/genkit';
import {z} from 'genkit';

const GenerateProcurementKitDescriptionInputSchema = z.object({
  keywords: z
    .string()
    .describe('Keywords describing the procurement kit (e.g., furniture, linen, appliances).'),
});
export type GenerateProcurementKitDescriptionInput = z.infer<
  typeof GenerateProcurementKitDescriptionInputSchema
>;

const GenerateProcurementKitDescriptionOutputSchema = z.object({
  description: z
    .string()
    .describe('A detailed description of the procurement kit generated by AI.'),
});
export type GenerateProcurementKitDescriptionOutput = z.infer<
  typeof GenerateProcurementKitDescriptionOutputSchema
>;

export async function generateProcurementKitDescription(
  input: GenerateProcurementKitDescriptionInput
): Promise<GenerateProcurementKitDescriptionOutput> {
  return generateProcurementKitDescriptionFlow(input);
}

const prompt = ai.definePrompt({
  name: 'generateProcurementKitDescriptionPrompt',
  input: {schema: GenerateProcurementKitDescriptionInputSchema},
  output: {schema: GenerateProcurementKitDescriptionOutputSchema},
  prompt: `You are an AI assistant designed to generate descriptions for procurement kits.

  Based on the provided keywords, create a comprehensive and engaging description that highlights the kit's contents and benefits.

  Keywords: {{{keywords}}}
  `,
});

const generateProcurementKitDescriptionFlow = ai.defineFlow(
  {
    name: 'generateProcurementKitDescriptionFlow',
    inputSchema: GenerateProcurementKitDescriptionInputSchema,
    outputSchema: GenerateProcurementKitDescriptionOutputSchema,
  },
  async input => {
    const {output} = await prompt(input);
    return output!;
  }
);
